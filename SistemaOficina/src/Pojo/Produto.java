
package Pojo;


public class Produto {
    private int     Codigo_Produto;
    private String  Nome_Peca;
    private int     Qntd;
    private int     Codigo_Cor;
    private int     Codigo_Marca;

    public int getCodigo_Produto() {
        return Codigo_Produto;
    }

    public void setCodigo_Produto(int Codigo_Produto) {
        this.Codigo_Produto = Codigo_Produto;
    }

    public String getNome_Peca() {
        return Nome_Peca;
    }

    public void setNome_Peca(String Nome_Peca) {
        this.Nome_Peca = Nome_Peca;
    }

    public int getQntd() {
        return Qntd;
    }

    public void setQntd(int Qntd) {
        this.Qntd = Qntd;
    }

    public int getCodigo_Cor() {
        return Codigo_Cor;
    }

    public void setCodigo_Cor(int Codigo_Cor) {
        this.Codigo_Cor = Codigo_Cor;
    }

    public int getCodigo_Marca() {
        return Codigo_Marca;
    }

    public void setCodigo_Marca(int Codigo_Marca) {
        this.Codigo_Marca = Codigo_Marca;
    }
    
    
}
