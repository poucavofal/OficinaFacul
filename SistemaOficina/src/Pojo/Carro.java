
package Pojo;


public class Carro {
    
    private int     Codigo_Marca;
    private int     Codigo_Cor;
    private int     Codigo_Carro;
    private String  Placa_Carro;
    private int     Codigo_Modelo;

    public int getCodigo_Marca() {
        return Codigo_Marca;
    }

    public void setCodigo_Marca(int Codigo_Marca) {
        this.Codigo_Marca = Codigo_Marca;
    }

    public int getCodigo_Cor() {
        return Codigo_Cor;
    }

    public void setCodigo_Cor(int Codigo_Cor) {
        this.Codigo_Cor = Codigo_Cor;
    }

    public int getCodigo_Carro() {
        return Codigo_Carro;
    }

    public void setCodigo_Carro(int Codigo_Carro) {
        this.Codigo_Carro = Codigo_Carro;
    }

    public String getPlaca_Carro() {
        return Placa_Carro;
    }

    public void setPlaca_Carro(String Placa_Carro) {
        this.Placa_Carro = Placa_Carro;
    }

    public int getCodigo_Modelo() {
        return Codigo_Modelo;
    }

    public void setCodigo_Modelo(int Codigo_Modelo) {
        this.Codigo_Modelo = Codigo_Modelo;
    }

    
}

    